/* Mix a brand colour with an alpha — keeps callers off raw rgba(). */
export function withOpacity(key: BrandKey, alpha: number) {
  const hex = (BRAND[key] || BRAND.ink).replace('#', '');
  const r = parseInt(hex.slice(0, 2), 16);
  const g = parseInt(hex.slice(2, 4), 16);
  const b = parseInt(hex.slice(4, 6), 16);
  return `rgba(${r},${g},${b},${alpha})`;
}

