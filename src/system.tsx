/* Treat http(s), mailto, tel, and any value starting with # as
   non-router targets — react-router's <Link to="#x"> would prefix
   the current path; plain <a href="#x"> just scrolls. */
function isExternalLink(to: string | undefined) {
  if (!to) return true;
  return /^(https?:|mailto:|tel:|#)/.test(to);
}

